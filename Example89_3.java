// Convert object of the Long class to int
//
class Example89_3 {

  public static void main(String[] args) {

    // creatr an object of Long class
    Long obj = 52341241L;

    // convert object of Long into int
    // using intValue()
    int a = obj.intValue();

    System.out.println(a);        // 52341241
  }
}
