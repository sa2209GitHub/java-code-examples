// Java Program to Convert Double to int
//
class Example96_3 {
  public static void main(String[] args) {

    // create an instance of Double
    Double obj = 78.6;

    // convert obj to int
    // using intValue()
    int num = obj.intValue();

    // print the int value
    System.out.println(num);    // 78
  }
}
